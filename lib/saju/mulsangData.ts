// 대분류별로 묶기 (화면 그룹 표시용) — questions.ts의 groupByCategory와 동일 형태
export function groupMulsangByCategory(
  list: SajuQuestion[] = MULSANG_QUESTIONS,
): { category: string; items: SajuQuestion[] }[] {
  const map = new Map<string, SajuQuestion[]>()
  for (const q of list) {
    if (!map.has(q.category)) map.set(q.category, [])
    map.get(q.category)!.push(q)
  }
  return Array.from(map.entries()).map(([category, items]) => ({ category, items }))
}
