// 이름 뒤에 "님"을 붙이는 공용 함수.
//
// [왜 필요한가]
//   보관함에 이름을 "나"로 저장한 경우 `${name}님` 이 "나님"이 되어 어색했다.
//   (2026-07-20 대운 화면에서 "나님의 대운" 으로 발견)
//   같은 구조가 앱 곳곳에 있어서 한 곳에서 규칙을 정하도록 모았다.
//
// [쓰는 법]
//   withNim('홍길동')        →  '홍길동님'
//   withNim('나')            →  '나'          (님 안 붙임)
//   withNim('')              →  ''            (빈 값은 그대로)
//   nimEuiTitle('나','대운')  →  '나의 대운'
//   nimEuiTitle('홍길동','대운') → '홍길동님의 대운'
// ----------------------------------------------------------------------------

// "님"을 붙이면 어색해지는 말들.
// 자기 자신을 가리키는 말이거나, 이미 높임말이 들어있는 경우.
const NO_NIM = new Set([
  '나', '저', '본인', '자신', '나님', '저님',
  '엄마', '아빠', '어머니', '아버지',
  '할머니', '할아버지', '누나', '언니', '형', '오빠', '동생',
])

/**
 * 이름 뒤에 "님"을 붙인다. 어색한 경우에는 붙이지 않는다.
 * 빈 값이면 빈 문자열을 돌려준다(호출한 쪽에서 판단하도록).
 */
export function withNim(name?: string | null): string {
  const n = (name ?? '').trim()
  if (!n) return ''
  if (NO_NIM.has(n)) return n
  if (n.endsWith('님')) return n      // 이미 "님"으로 끝나면 두 번 붙이지 않는다
  return `${n}님`
}

/**
 * "OO님의 대운" / "나의 대운" 같은 제목을 만든다.
 * 이름이 없거나 자기 자신을 뜻하면 "나의 ~" 로 적는다.
 */
export function nimEuiTitle(name: string | null | undefined, tail: string): string {
  const n = (name ?? '').trim()
  if (!n || NO_NIM.has(n)) return `나의 ${tail}`
  return `${withNim(n)}의 ${tail}`
}
