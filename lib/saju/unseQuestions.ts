// 홈에서 들어오는 진입점은 "대운"과 "세운" 둘뿐이다.
// (월운은 독립 버튼 없이 세운 안에 함께 들어간다 — 세운=올해, 월운=올해 중 몇 월이라 붙는다.)
export type UnseEntry = 'daeun' | 'seyun'

export const UNSE_ENTRY_LABEL: Record<UnseEntry, string> = {
  daeun: '대운 (10년 흐름)',
  seyun: '세운 (올해·달별)',
}

// 진입점(대운/세운)에 보여줄 질문을 반환한다.
//   - daeun → 대운 질문만
//   - seyun → 세운 질문 + 월운 질문 (한 화면에서 올해·달별 함께)
// enabled=true 인 것만. 순서는 UNSE_QUESTIONS 정의 순(대운→세운→월운) 유지.
export function unseQuestionsForEntry(entry: UnseEntry): UnseQuestion[] {
  const kinds: UnseKind[] = entry === 'seyun' ? ['seyun', 'wolun'] : ['daeun']
  return UNSE_QUESTIONS.filter((q) => q.enabled && kinds.includes(q.kind))
}

// (하위호환) 단일 kind로 필터가 필요할 때. 화면은 위 Entry 함수를 쓴다.
export function unseQuestionsFor(kind: UnseKind): UnseQuestion[] {
  return UNSE_QUESTIONS.filter((q) => q.enabled && q.kind === kind)
}

// 대분류별로 묶기 (화면 그룹 표시용) — questions.ts의 groupByCategory와 동일 형태
export function groupUnseByCategory(
  list: UnseQuestion[],
): { category: string; items: UnseQuestion[] }[] {
  const map = new Map<string, UnseQuestion[]>()
  for (const q of list) {
    if (!map.has(q.category)) map.set(q.category, [])
    map.get(q.category)!.push(q)
  }
  return Array.from(map.entries()).map(([category, items]) => ({ category, items }))
}

// 세운 화면에서 "올해(세운) / 월별(월운)" 소제목으로 나눌 때 쓰는 그룹.
//   세운 진입이면 [세운 그룹, 월운 그룹] 두 덩어리, 대운 진입이면 대운 한 덩어리.
//   각 덩어리 안은 다시 대분류(category)로 묶는다.
export function groupUnseByKind(
  list: UnseQuestion[],
): { kind: UnseKind; label: string; groups: { category: string; items: UnseQuestion[] }[] }[] {
  const order: UnseKind[] = ['daeun', 'seyun', 'wolun']
  return order
    .map((kind) => {
      const items = list.filter((q) => q.kind === kind)
      return {
        kind,
        label: kind === 'seyun' ? '올해' : kind === 'wolun' ? '올해 중 몇 월' : '대운',
        groups: groupUnseByCategory(items),
      }
    })
    .filter((g) => g.groups.length > 0)
}
