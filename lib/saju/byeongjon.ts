/**
 * 사주 네 기둥에서 병존을 찾는다.
 *   기준: 같은 글자 2개 이상 (붙어 있든 떨어져 있든) — 2026-07-19 확정
 *
 * @param stems   네 기둥 천간 [년, 월, 일, 시]
 * @param branches 네 기둥 지지 [년, 월, 일, 시]
 */
export function findByeongjon(stems: string[], branches: string[]): ByeongjonInfo[] {
  const found: ByeongjonInfo[] = []

  const countOf = (arr: string[]) => {
    const m = new Map<string, number>()
    for (const c of arr) if (c && c !== '?') m.set(c, (m.get(c) ?? 0) + 1)
    return m
  }

  for (const [char, n] of countOf(stems)) {
    if (n < 2) continue
    const info = STEM_BYEONGJON[char + char]
    if (info) found.push(info)
  }
  for (const [char, n] of countOf(branches)) {
    if (n < 2) continue
    const info = BRANCH_BYEONGJON[char + char]
    if (info) found.push(info)
  }
  return found
}

