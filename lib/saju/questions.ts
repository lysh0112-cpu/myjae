// 나이(만) → 연령대 코드
export function ageToGroup(age: number): AgeGroup {
  if (age < 30) return '20s'
  if (age < 40) return '30s'
  if (age < 50) return '40s'
  if (age < 60) return '50s'
  return '60s'
}

// 생년(년도)으로 대략 만나이 → 연령대. (정밀 만나이는 생월일 필요하나 통변엔 이 정도로 충분)
export function birthYearToGroup(birthYear: number, now = new Date()): AgeGroup {
  return ageToGroup(now.getFullYear() - birthYear)
}

// 성별 문자열("남"/"여") → 필터 코드
export function genderToFilter(g: string): 'male' | 'female' {
  return g === '여' ? 'female' : 'male'
}

// 그 사람(연령대+성별)에게 보여줄 질문만 필터 (enabled=true + 성별 매칭)
export function questionsFor(age: AgeGroup, gender: 'male' | 'female'): SajuQuestion[] {
  return QUESTIONS.filter(q =>
    q.enabled &&
    q.age === age &&
    (q.gender === 'all' || q.gender === gender)
  )
}

// 대분류별로 묶기 (화면 그룹 표시용)
export function groupByCategory(list: SajuQuestion[]): { category: string; items: SajuQuestion[] }[] {
  const map = new Map<string, SajuQuestion[]>()
  for (const q of list) {
    if (!map.has(q.category)) map.set(q.category, [])
    map.get(q.category)!.push(q)
  }
  return Array.from(map.entries()).map(([category, items]) => ({ category, items }))
}
