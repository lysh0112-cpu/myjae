// lib/saju/coupleJijiText.ts
// ============================================================================
//  두 분 일지가 만나는 자리 — 화면 문구
//
//  ★ 왜 이 파일이 따로 있나
//    jijiGrade.ts(심산 49쪽 + 51~73쪽, 연재쌤 3칸 확정)는 144칸 전부에
//    등급·태그·해설이 들어 있고, 그중 139칸은 궁합에 그대로 써도 된다.
//    그런데 5칸이 대운·세운 이야기라 궁합 맥락과 어긋난다.
//      예) 辰→寅 "운에서 辰土가 오면 급성장을 하려는 욕망이 강하게 나타난다"
//          → 궁합은 두 사람이 만나는 것이지 운을 보는 게 아니다.
//    그 5칸만 궁합용 문장으로 바꿔 쓴다. 나머지는 원문을 그대로 쓴다.
//
//  ★ 왜 144개를 다 새로 쓰지 않았나
//    원문이 있는데 전부 새로 지으면 심산 판정이 아니라 창작이 된다.
//    연재쌤 검수 부담도 144개가 된다. 5개면 검수가 가볍다.
//
//  ★ 어미 변환을 기계적으로 하지 않는 이유
//    원문 어미가 '한다/된다/있다/좋다/이다/해라/마라…' 등 16종이 넘고
//    "…이드된다" 같은 변형도 있다. 자동 치환하면 말이 깨진다.
//    그래서 문장은 원문 그대로 두고, 앞에 상황 설명을 한 줄 붙여
//    화면 흐름이 끊기지 않게 한다.
//
//  ⚠️ 화면에 쪽수·유파명을 쓰지 않는다. 근거는 이 주석에만 남긴다.
// ============================================================================

import { JIJI_GRADE, type JijiGrade } from './jijiGrade'

/** 궁합용으로 바꿔 쓰는 칸 — 키는 `${내지지}${상대지지}` */
const COUPLE_OVERRIDE: Record<string, string> = {
  // ── 子 → 午 (D · 子午沖) ──
  //   원문은 대운 겹충 + 조울증·우울증 이야기. 병명은 고객 화면에 쓰지 않는다.
  //   반대 방향(午→子)의 "감정 기복"이라는 뜻만 살렸다.
  子午:
    '일지가 子와 午로 정면에서 마주 보는 충(沖) 관계예요. ' +
    '물과 불이 맞서는 자리라 감정이 크게 오르내리기 쉽습니다. ' +
    '말이 뜨거워질 때 한 박자 쉬어 가시면 훨씬 편안해집니다.',

  // ── 丑 → 巳 (A · 가합) ──
  //   원문의 "응축된 기운이 활기차진다"를 궁합으로 옮겼다. 상승운 표현은 뺐다.
  丑巳:
    '일지가 丑과 巳로 서로를 끌어당기는 가합(假合) 관계예요. ' +
    '웅크리고 있던 기운이 상대를 만나 활기를 얻는 자리입니다. ' +
    '함께 있을 때 더 크게 움직이시는 사이예요.',

  // ── 辰 → 寅 (C · 격각) ──
  //   반대 방향(寅→辰)의 "寅과 辰 사이에 卯가 있다. 동상이몽"이 이 관계의 핵심.
  辰寅:
    '일지 辰과 寅 사이에 卯가 하나 비어 있는 격각(隔角) 관계예요. ' +
    '크게 부딪히지는 않지만, 같은 곳을 보면서도 마음속 그림이 ' +
    '조금씩 다를 수 있습니다. 서로의 생각을 자주 말로 나누시면 그 틈이 메워집니다.',

  // ── 申 → 卯 (C · 卯申귀문) ──
  //   반대 방향(卯→申)의 "암합을 하는 것은 긍정적으로 본다"를 살렸다.
  //   미움과 정이 함께 있는 것이 이 관계의 결이다. 주식 투자 이야기는 뺐다.
  申卯:
    '일지가 卯와 申으로 만나는 귀문(鬼門) 관계예요. ' +
    '겉으로는 멀쩡한데 속으로 서로에게 예민해지기 쉬운 자리입니다. ' +
    '다만 지장간에서 몰래 합을 이루고 있어, 미워하면서도 놓지 못하는 정이 함께 있는 사이예요.',

  // ── 戌 → 丑 (D · 丑戌刑) ──
  //   원문의 "土沖은 朋沖(벗끼리의 충)"을 "가까운 사이일수록"으로 옮겼다.
  //   배신수·손재수는 뺐다.
  戌丑:
    '일지가 戌과 丑으로 만나는 형(刑) 관계예요. ' +
    '같은 흙끼리 부딪히는 자리라, 가까운 사이일수록 서운함이 쌓이기 쉽습니다. ' +
    '담아두지 마시고 그때그때 풀어내시는 편이 두 분께 좋습니다.',
}

export interface JijiPairText {
  grade: JijiGrade
  tag: string
  /** 화면에 그대로 쓰는 문장 */
  text: string
  /** 궁합용으로 바꿔 쓴 칸인지 (검수·디버깅용) */
  overridden: boolean
}

/**
 * 내 일지가 상대 일지를 만났을 때의 문구.
 *
 * ⚠️ 비대칭이다. (辰→寅 은 C, 寅→辰 은 B)
 *    "누가 보느냐"에 따라 등급도 문장도 다르다. 한쪽만 쓰면 안 된다.
 */
export function jijiPairText(mine: string, other: string): JijiPairText | null {
  const row = JIJI_GRADE[mine]
  const cell = row?.[other]
  if (!cell) return null

  const key = `${mine}${other}`
  const ov = COUPLE_OVERRIDE[key]
  return {
    grade: cell.grade,
    tag: cell.tag,
    text: ov ?? cell.desc,
    overridden: !!ov,
  }
}

/** 등급 → 별 (A가 가장 좋음). 심산 49쪽 등급을 그대로 옮긴 것. */
export function jijiStars(grade: JijiGrade): 1 | 2 | 3 | 4 | 5 {
  switch (grade) {
    case 'A': return 4
    case 'B': return 3
    case 'C': return 2
    default: return 1     // D
  }
}
